export type EnqueueDriveReferenceDocsResult = {
  ok: boolean;
  enqueued: number;
  skippedAlreadyQueued: number;
  failed: number;
};

/**
 * Enqueue all 12 reference docs into drivePushQueue. Idempotent on
 * (target_folder, target_subpath, file_name, contentText) — re-running
 * with the same seed is a safe no-op. Fire-and-forget; never throws.
 */
export async function enqueueDriveReferenceDocs(
  seed: DriveReferenceSeed = {},
): Promise<EnqueueDriveReferenceDocsResult> {
  const docs = buildDriveReferenceDocs(seed);
  let enqueued = 0;
  let skippedAlreadyQueued = 0;
  let failed = 0;
  try {
    const [{ getDb }, schemaMod, drizzle] = await Promise.all([
      import("../db"),
      import("../../drizzle/schema"),
      import("drizzle-orm"),
    ]);
    const drivePushQueue = (schemaMod as any).drivePushQueue;
    const { and, eq } = drizzle as any;
    const db = getDb();

    for (const doc of docs) {
      try {
        let alreadyQueued = false;
        try {
          const existing: any[] = await db
            .select()
            .from(drivePushQueue)
            .where(
              and(
                eq(drivePushQueue.targetFolder as any, doc.targetFolder as any),
                eq(drivePushQueue.fileName as any, doc.fileName as any),
                eq(drivePushQueue.status as any, "pending" as any),
              ),
            );
          if (existing.some((r: any) => r?.contentText === doc.content)) {
            alreadyQueued = true;
          }
        } catch {
          // best-effort dedupe; fall through
        }
        if (alreadyQueued) {
          skippedAlreadyQueued += 1;
          continue;
        }
        await db.insert(drivePushQueue).values({
          targetFolder: doc.targetFolder as any,
          targetSubpath: doc.targetSubpath,
          fileName: doc.fileName,
          mimeType: "text/markdown",
          contentText: doc.content,
          status: "pending" as any,
        } as any);
        enqueued += 1;
      } catch (e) {
        failed += 1;
        console.warn(
          `[enqueueDriveReferenceDocs] failed for ${doc.slug}`,
          (e as any)?.message ?? e,
        );
      }
    }
    return { ok: true, enqueued, skippedAlreadyQueued, failed };
  } catch (e: any) {
    console.warn("[enqueueDriveReferenceDocs] fatal", e?.message ?? e);
    return { ok: false, enqueued, skippedAlreadyQueued, failed: docs.length - enqueued - skippedAlreadyQueued };
  }
}
