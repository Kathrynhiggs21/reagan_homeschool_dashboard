/**
 * Twelve reference Markdown docs that get pushed to canonical Drive
 * subfolders so Mom, Dad, Grandma, and any tutor can read them inside
 * Drive without needing to log into the dashboard.
 *
 * Each doc is a pure builder (no DB/IO) — the file body is fully
 * deterministic given the dateISO + dashboardUrl seed. The enqueuer
 * inserts a `drivePushQueue` row per doc, idempotent on contentText
 * match (same body already pending for the same path → no-op). The
 * Drive worker drains the queue and writes the actual files.
 *
 * Coverage map (12 docs):
 *   1.  Ohio 5th-Grade Scope & Sequence (curriculum_checklist)
 *   2.  Ohio IH Annual Portfolio — How To (report_cards)
 *   3.  Weekly Cadence at a Glance (daily_schedule)
 *   4.  Khan Academy: What Maps Where (apps_tools)
 *   5.  IXL: What Maps Where (apps_tools)
 *   6.  Kiwi the Mascot — Tap Cheatsheet (kiwi_coins)
 *   7.  Adult Quick-Entry — One-Tap Logging (adult_notes)
 *   8.  Recap Email — How To Reply (recap_reply)
 *   9.  Day Log Format — What's In Each File (day_log)
 *   10. Tutor Handoff Template (tutor)
 *   11. Bookshelf & Reading Log Conventions (bookshelf)
 *   12. Adventures: Outdoor Block Playbook (adventures)
 */

export type DriveReferenceDoc = {
  /** Stable slug used for dedupe lookups. */
  slug: string;
  /** drivePushQueue.target_folder enum value. */
  targetFolder: string;
  /** subfolder under the target (kept stable; not date-bucketed). */
  targetSubpath: string;
  /** Filename, including extension. */
  fileName: string;
  /** Markdown body (deterministic given seed). */
  content: string;
};

export type DriveReferenceSeed = {
  generatedAtISO?: string;
  dashboardUrl?: string;
};

const FOOTER = (generatedAtISO: string, dashboardUrl: string) =>
  `\n\n---\n*Generated by Reagan's Homeschool Dashboard on ${generatedAtISO}. ` +
  `Live source of truth: ${dashboardUrl}*\n`;

